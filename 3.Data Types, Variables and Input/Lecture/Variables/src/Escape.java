public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello KG Coding.....\t hello");
        System.out.println("Hello KG Coding.....\b hello");
        System.out.println("Hello KG Coding.....\n hello");
        System.out.println("Hello KG Coding.....'hello'");
        System.out.println("Hello KG Coding.....\"hello\"");
        System.out.println("Hello KG Coding.....\\hello\\");
    }
}
